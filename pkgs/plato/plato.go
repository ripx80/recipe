/*Entry with plato values in float*/
type Entry struct {
	SG  float64
	SW  float64
	VOL float64
	AF  float64
}

/*Table with Plato values with multiple entries*/
type Table [200]Entry

//SW return Entry looked up by sw
func (t Table) SW(sw float64) (*Entry, error) {
	for _, v := range t {
		if v.SW == sw {
			return &v, nil
		}
	}
	return nil, fmt.Errorf("entry not found")
}

//AF return Entry looked up by sw
func (t Table) AF(af float64) (*Entry, error) {
	for _, v := range t {
		if v.AF == af {
			return &v, nil
		}
	}
	return nil, fmt.Errorf("entry not found")
}

//SG return Entry looked up by sw
func (t Table) SG(sg float64) (*Entry, error) {
	for _, v := range t {
		if v.SG == sg {
			return &v, nil
		}
	}
	return nil, fmt.Errorf("entry not found")
}

